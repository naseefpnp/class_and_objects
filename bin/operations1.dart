///1. arithmetic opration +,-,*,/,%,~/
void main(){
    int a=100;
    int b=35;

    print("a + b=${a+b}");
    print("a - b=${a-b}");
    print("a * b=${a*b}");
    print("a / b=${a/b}");
    print("a % b=${a%b}");//to fatch remaider
    print("a ~ b=${a~/b}");

    ///2.ASSIGMENT OPERATOR =, +=,-=,*= ..........etc
    dynamic x =20, y =30;
    print("x = y  -> ${x=y}");
    print("x += y  -> ${x+=y}");
    print("x-= y -> ${x-=y}");
    print("x *= y -> ${x*=y}");
    print("x /= y -> ${x/=y}");

}